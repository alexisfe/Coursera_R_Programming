##The following two functions allow to cache matrix inverse computation which helps make repeated expensive computations much more time-efficient

## Creates a special matrix that is  really a list containing functions to set, get, setinverse and getinverse.

makeCacheMatrix <- function(x = matrix()) {
  i <- NULL
  set <- function(y) {
    x <<- y
    i <<- NULL
  }
  get <- function() x
  setinverse <- function(inverse) i <<- inverse
  getinverse <- function() i
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)
}

## Attempts to fetch from cache the inverse of a matrix, otherwise calculate the inverse and caches the result

cacheSolve <- function(x, ...) {
  i <- x$getinverse()
  if(!is.null(i)) {
    message("getting cached data")
    return(i)
  }
  data <- x$get()
  i <- solve(data)
  x$setinverse(i)
  i
}
